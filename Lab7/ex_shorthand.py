# shorthand


x = 100

# shorthand if
if x>50:
    print('x > 50')
    print('test')

# shorthand if-else
if x>50:
    print('x > 50')
else:
    print('x < 50')

print('x > 50') if x>50 else print('x < 50')

# shorthand if-else-if
if x<50:
    print('x < 50')
elif x>50:
    print('x > 50')
else:
    print('x = 50')

